# Function to calculate the sum of elements in a list
def sum_elements(elements):
    total = sum(elements)  # Using Python's built-in sum function to calculate the total sum
    print(f"The sum of all elements is: {total}")

# Example usage
elements_list = [10, 20, 30, 40, 50]  # You can modify this list with any set of numbers
sum_elements(elements_list)
